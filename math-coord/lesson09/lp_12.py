from scipy.optimize import linprog

c = [-2 , -9, -6]  # Цільова функція - на мінімум
A_ub = [
    [12, 6,2],
    [12, 24,18],
    [12, 12,12],
    [-1, 0,0],
    [0,-1, 0],
    [0,0, -1],
]  # Коефіцієнти при нерівностях
b_ub = [360,192,180,0,0,0]  # Результати при нерівностях

print(linprog(c, A_ub, b_ub))  # Оптимізація
